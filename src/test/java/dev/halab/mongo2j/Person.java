package dev.halab.mongo2j;

import dev.halab.mongo2j.annotation.DocumentId;

/**
 * @author Apollo
 */
public class Person {

    @DocumentId
    private String id;
    private String name;

    public Person() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                '}';
    }
}
